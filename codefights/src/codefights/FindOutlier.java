	static int find(int[] i) {
		int[] parity = new int[2];
		int p = -1;
		for (int a = 0; a < i.length; a++) {
			int h = Math.abs(i[a] % 2);
			if (parity[h] == 0)
				parity[h] = a + 1;
			else
				p = h;
			if (p != -1 && parity[(p + 1) % 2] != 0)
				return i[parity[(p + 1) % 2] - 1];
		}
		return 0;
	}
